export const getTeacher = (subject, lang = 'id') => {
    const teacherData = teachers[subject];
    if (!teacherData) return "Unknown Teacher";

    if (typeof teacherData === 'string') return teacherData;
    return teacherData[lang] || teacherData['id'] || "Unknown Teacher";
};

export const getSubjectName = (subject, lang) => {
    if (!translations[lang] || !translations[lang].subjects) return subject;
    return translations[lang].subjects[subject] || subject;
};
